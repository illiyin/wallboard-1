        <!-- Progressbar -->
        <script type="text/javascript">
            window.setInterval(function(){
                var skillBar = $('.progressbar').find('.inner');
                var proValue = getRandomArbitrary(0, 100);
                var nowValue = Math.round(proValue);
                var num      = $('#progval');
                
                //remove animation on progress box
//                if(num.hasClass('animated', 'fadeIn')){
//                    num.removeClass('animated ' + 'fadeIn');
//                }
                
                //change progressbar with random value
                skillBar.attr("data-progress", nowValue + "%");
                
                var skillVal = skillBar.attr("data-progress");
                var nominal  = skillVal.split('%').join('');
                
                if(nominal > 89){
                    skillBar.addClass('good-bar');
                    skillBar.removeClass('warn-bar');
                    $('#back1').animate({opacity: 0}, 1000);
//                    $('#back2').animate({opacity: 0}, 1000);
                }else{
                    skillBar.addClass('warn-bar');
                    skillBar.removeClass('good-bar');
                    $('#back1').animate({opacity: 1}, 1000);
//                    $('#back2').animate({opacity: 1}, 1000);
                }
                
                $(skillBar).animate({
                    height: skillVal
                }, 1200);
                
                num.text(nominal);
                
                function getRandomArbitrary(min, max) {
                    return Math.random() * (max - min) + min;
                }
            }, 5000);
            
        </script>
        
